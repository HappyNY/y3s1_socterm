skConsole::skConsole(size_t Width, size_t Height) :
	mWidth(Width),
	mHeight(Height),
	mBuff(new uint16_t(Width * Height)), 
	mCsrX(0),
	mCsrY(0),
	ForeColor(0xffff),
	BackColor(0x0000)
{
	memset(mBuff, 0, Width*Height*sizeof(*mBuff));
}

skConsole::~skConsole()
{
	delete mBuff;
}

void skConsole::Write(char* str)
{ 
	while(*str != 0)
	{
		WriteChar(*str++);
	}
}

